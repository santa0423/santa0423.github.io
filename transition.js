// button 클래스 요소 클릭 시, #object1, #object2 확대 애니메이션 진행 후 링크를 오픈
$('.submenu1-1').on('click', function(){
    console.log('clicked...');
    $('#object1').animate({
        width: 10000,
        height: 6000
    }, 700); // 애니메이션 시간
    $('#object2').delay(500).animate({ // 딜레이 시간
        width: 10000,
        height: 6000
    }, 
    700, // 애니메이션 시간
    function(){
        window.open('transition2.html', '_self');
    }
);
});
